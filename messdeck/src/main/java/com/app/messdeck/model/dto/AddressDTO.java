package com.app.messdeck.model.dto;

import java.util.regex.Pattern;

import net.sf.oval.constraint.MatchPattern;

public class AddressDTO extends AbstractDTO {

	private String street;

	@MatchPattern(pattern = "Pune", flags = {
			Pattern.CASE_INSENSITIVE }, message = "Only Supported in Pune. Valid values list [Pune]")
	private String city;

	@MatchPattern(pattern = "Maharashtra", flags = {
			Pattern.CASE_INSENSITIVE }, message = "Only Supported in Maharashtra. Valid values list [Maharashtra]")
	private String state;

	@MatchPattern(pattern = "india", flags = {
			Pattern.CASE_INSENSITIVE }, message = "Only Supported in India. Valid values list [India]")
	private String country;

	@MatchPattern(pattern = "^[1-9][0-9]{5}$", message = "Invalid Pincode")
	private String pinCode;

	public AddressDTO() {
	}

	public String getStreet() {
		return street;
	}

	public void setStreet(String street) {
		this.street = street;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getPinCode() {
		return pinCode;
	}

	public void setPinCode(String pinCode) {
		this.pinCode = pinCode;
	}

	@Override
	public String toString() {
		return "Address [street=" + street + ", city=" + city + ", state=" + state + ", country=" + country
				+ ", pinCode=" + pinCode + "]";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((city == null) ? 0 : city.hashCode());
		result = prime * result + ((country == null) ? 0 : country.hashCode());
		result = prime * result + ((pinCode == null) ? 0 : pinCode.hashCode());
		result = prime * result + ((state == null) ? 0 : state.hashCode());
		result = prime * result + ((street == null) ? 0 : street.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		AddressDTO other = (AddressDTO) obj;
		if (city == null) {
			if (other.city != null)
				return false;
		} else if (!city.equals(other.city))
			return false;
		if (country == null) {
			if (other.country != null)
				return false;
		} else if (!country.equals(other.country))
			return false;
		if (pinCode == null) {
			if (other.pinCode != null)
				return false;
		} else if (!pinCode.equals(other.pinCode))
			return false;
		if (state == null) {
			if (other.state != null)
				return false;
		} else if (!state.equals(other.state))
			return false;
		if (street == null) {
			if (other.street != null)
				return false;
		} else if (!street.equals(other.street))
			return false;
		return true;
	}

}
